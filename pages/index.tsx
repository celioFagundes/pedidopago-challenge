import type { GetServerSideProps } from 'next'
import Head from 'next/head'
import axios from 'axios'
import { useEffect, useRef, useState } from 'react'
import Table from '../components/Table/index'
import { BsThreeDotsVertical } from 'react-icons/bs'
import Layout from '../components/Layout'
import {
  BottomContainer,
  Container,
  Content,
  ListTitle,
  ModalButton,
  ModalMenu,
  ModalOption,
  ModalOptionIcon,
  ModalOptionLink,
  PageTitle,
  Tab,
  Tabs,
  Wrapper,
} from './home'
import Link from 'next/link'
import { useRouter } from 'next/router'
import SearchInput from '../components/SearchInput'
import SelectQuantity from '../components/Select'
import Pagination from '../components/Pagination'
import { AiOutlineEye } from 'react-icons/ai'
import { RiDeleteBinLine } from 'react-icons/ri'
interface Agent {
  agent_id: number
  name: string
  image: string
  department: string
  branch: string
  role: string
  status: string
}
interface MainProps {
  data: [Agent]
}

interface ModalIsOpenList {
  [key: number]: boolean
}
const Home: React.FC<MainProps> = ({ data }) => {
  const [displayData, setDisplayData] = useState(data)
  const [modalIsOpenList, setModalIsOpenList] = useState<ModalIsOpenList>({})
  const router = useRouter()

  useEffect(() => {
    const modalList = displayData.reduce((prev, curr) => {
      return { ...prev, [curr.agent_id]: false }
    }, {})
    setModalIsOpenList(modalList)
  }, [displayData])

  const toggleModal = (id: number) => {
    let newList = { ...modalIsOpenList }
    Object.keys(newList).forEach(item => (newList[Number(item)] = false))
    newList[id] = true
    setModalIsOpenList(newList)
  }
  const anyModalOpen = () => {
    let isOpen = false
    Object.keys(modalIsOpenList).forEach(item => {
      if (modalIsOpenList[Number(item)]) {
        isOpen = true
      }
    })
    return isOpen
  }
  const closeModal = () => {
    if (anyModalOpen()) {
      let newList = { ...modalIsOpenList }
      Object.keys(newList).forEach(item => (newList[Number(item)] = false))
      setModalIsOpenList(newList)
    }
  }

  return (
    <div style={{ width: '100%' }}>
      <Head>
        <title>Create Next App</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>
      <Layout>
        <Wrapper onClick={closeModal}>
          <Container>
            <PageTitle>Organização</PageTitle>
            <Content>
              <Tabs>
                <Link href='/'>
                  <Tab isActive={router.pathname === '/'}>Colaboradores</Tab>
                </Link>
                <Link href='/cargos'>
                  <Tab isActive={router.pathname !== '/'}>Cargos</Tab>
                </Link>
              </Tabs>
              <SearchInput />
              <ListTitle>Listagem de colaboradores</ListTitle>
              {data && (
                <Table>
                  <Table.Header>
                    <Table.Row>
                      <Table.Th>Nome Completo</Table.Th>
                      <Table.Th>Departamento</Table.Th>
                      <Table.Th>Cargo</Table.Th>
                      <Table.Th>Unidade</Table.Th>
                      <Table.Th>Status</Table.Th>
                      <Table.Th></Table.Th>
                    </Table.Row>
                  </Table.Header>
                  <Table.Body>
                    {data.map(
                      agent =>
                        agent.agent_id !== 15 && (
                          <Table.Row key={agent.agent_id}>
                            <Table.Td status={agent.status}>
                              <Table.AvatarAndName url={agent.image} name={agent.name} />
                            </Table.Td>
                            <Table.Td status={agent.status}>{agent.department}</Table.Td>
                            <Table.Td status={agent.status}>{agent.role}</Table.Td>
                            <Table.Td status={agent.status}>{agent.branch}</Table.Td>
                            <Table.Td>
                              <Table.Status status={agent.status}>
                                {agent.status === 'active' ? 'Ativo' : 'Inativo'}
                              </Table.Status>
                            </Table.Td>
                            <Table.Td>
                              <ModalButton onClick={() => toggleModal(agent.agent_id)}>
                                <BsThreeDotsVertical size={16} />
                              </ModalButton>

                              {modalIsOpenList[agent.agent_id] && (
                                <ModalMenu>
                                  <Link href={`/colaborador/${agent.agent_id}`}>
                                    <ModalOption>
                                      <ModalOptionIcon>
                                        <AiOutlineEye size={22} />
                                      </ModalOptionIcon>

                                      <ModalOptionLink isActive={true}>
                                        Ver colaborador
                                      </ModalOptionLink>
                                    </ModalOption>
                                  </Link>
                                  <Link href='#'>
                                    <ModalOption>
                                      <ModalOptionIcon>
                                        <RiDeleteBinLine size={22} />
                                      </ModalOptionIcon>

                                      <ModalOptionLink isActive={false}>Excluir</ModalOptionLink>
                                    </ModalOption>
                                  </Link>
                                </ModalMenu>
                              )}
                            </Table.Td>
                          </Table.Row>
                        )
                    )}
                  </Table.Body>
                </Table>
              )}
              <BottomContainer>
                <SelectQuantity />
                <Pagination />
              </BottomContainer>
            </Content>
          </Container>
        </Wrapper>
      </Layout>
    </div>
  )
}

export const getServerSideProps: GetServerSideProps = async context => {
  const { data } = await axios.get('https://pp-api-desafio.herokuapp.com/agents')
  return {
    props: {
      data: data.items,
    },
  }
}

export default Home
